package com.kubg.domain;

public class CuGoodsVO {
	
	private int cuNaum;
	private String cuName;
	private int cuStock;
	private String cuPrice;
	private String cuImg;
	private String cuThumbImg;
	
	
	
	
	public String getCuThumbImg() {
		return cuThumbImg;
	}
	public void setCuThumbImg(String cuThumbImg) {
		this.cuThumbImg = cuThumbImg;
	}
	public int getCuNaum() {
		return cuNaum;
	}
	public void setCuNaum(int cuNaum) {
		this.cuNaum = cuNaum;
	}
	public String getCuName() {
		return cuName;
	}
	public void setCuName(String cuName) {
		this.cuName = cuName;
	}
	public int getCuStock() {
		return cuStock;
	}
	public void setCuStock(int cuStock) {
		this.cuStock = cuStock;
	}
	public String getCuPrice() {
		return cuPrice;
	}
	public void setCuPrice(String cuPrice) {
		this.cuPrice = cuPrice;
	}
	public String getCuImg() {
		return cuImg;
	}
	public void setCuImg(String cuImg) {
		this.cuImg = cuImg;
	}
	
	
}
